package book01

import "fmt"

/**

给你两个字符串haystack 和 needle, 请你在 haystack 字符串中找出 needle 字符串的第一个匹配项的下标（下标从 0 开始）. 如果 needle 不是 haystack 的一部分, 则返回 -1 .

示例 1:
	输入: haystack = "sadbutsad", needle = "sad"
	输出: 0
	解释: "sad" 在下标 0 和 6 处匹配. 第一个匹配项的下标是 0 , 所以返回 0.

示例 2:
	输入: haystack = "leetcode", needle = "leeto"
	输出: -1
	解释: "leeto" 没有在 "leetcode" 中出现，所以返回 -1 。

提示：
	1. 1 <= haystack.length, needle.length <= 104
	2. haystack 和 needle 仅由小写英文字符组成

作者：力扣 (LeetCode)
链接：https://leetcode.cn/leetbook/read/top-interview-questions-easy/xnr003/
来源：力扣（LeetCode）
著作权归作者所有。商业转载请联系作者获得授权，非商业转载请注明出处。

*/

func strStr(haystack string, needle string) int {

	n, m := len(haystack), len(needle)
outer:
	for i := 0; i+m <= n; i++ {
		for j := range needle {
			fmt.Println(j)
			if haystack[i+j] != needle[j] {
				continue outer
			}
		}
		return i
	}
	return -1
}
